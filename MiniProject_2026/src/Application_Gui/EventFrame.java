    public static void loadEventsToTable() {
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) Event_Display_Table.getModel();
    model.setRowCount(0);
    for (Classes.Event e : Event_List) {
        Object[] row = {
            e.code,
            e.title,
            e.date,
            e.startHour + ":00 - " + e.endHour + ":00",
            e.maxParticipants
        };
        model.addRow(row);
    }
}

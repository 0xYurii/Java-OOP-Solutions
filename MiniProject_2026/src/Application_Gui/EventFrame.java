    private void Delete_Event_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Delete_Event_BtnActionPerformed
            int selectedRow=Event_Display_Table.getSelectedRow();
            if (selectedRow == -1) {
                System.out.println("System Error: No room selected.");
                return;
            }
            Event_List.remove(selectedRow);
            loadEventsToTable();
    }//GEN-LAST:event_Delete_Event_BtnActionPerformed

    private void Search_Event_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Search_Event_BtnActionPerformed
        String keyword=Search_Event_TextField.getText().toLowerCase().trim();
           
        if (keyword.isEmpty()) {
            loadEventsToTable();
            return;
        }
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) Event_Display_Table.getModel();
        model.setRowCount(0); 
        
        
        for (Classes.Event e : Event_List) {
            if (e.title.contains(keyword) || e.code.contains(keyword)|| e.date.contains(keyword)) {
                Object[] row = { e.code, e.title, e.date,e.startHour,e.endHour,e.maxParticipants };
                model.addRow(row);
            }
        }
        System.out.println("System: Search executed for '" + keyword + "'");    
    }//GEN-LAST:event_Search_Event_BtnActionPerformed

